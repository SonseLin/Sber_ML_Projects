Уровень изоляции транзакций SERIALIZABLE в PostgreSQL обеспечивает максимальную изоляцию, 
гарантируя, что параллельные транзакции будут исполняться так, 
как будто они выполняются последовательно, одна за другой. 
Это предотвращает феномены, такие как грязные чтения, неповторяющиеся чтения и фантомные чтения.

